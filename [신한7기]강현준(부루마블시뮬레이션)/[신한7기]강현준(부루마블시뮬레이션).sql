--------------------------------------------------------
--  TABLE USERS
--------------------------------------------------------

CREATE TABLE USERS (
    USER_ID     VARCHAR2(20),
    PASSWORD    VARCHAR2(100) NOT NULL,
    USER_NAME   VARCHAR2(20) NOT NULL,
    QUESTION    VARCHAR2(100) NOT NULL,
    ANSWER      VARCHAR2(20) NOT NULL,
    GOLD        NUMBER DEFAULT 0 NOT NULL,
    EXPERIENCE  NUMBER DEFAULT 0 NOT NULL,

    CONSTRAINT USERS_PK PRIMARY KEY (USER_ID)
);


--------------------------------------------------------
--  TABLE ITEM
--------------------------------------------------------

CREATE TABLE ITEM (
    ITEM_ID       NUMBER(10, 0),
    ITEM_NAME     VARCHAR2(20) NOT NULL,
    PRICE         NUMBER(10, 0) DEFAULT 0 NOT NULL,
    DESCRIPTION   VARCHAR2(100),
    EFFECT_TYPE   VARCHAR2(20),
    EFFECT_VALUE  NUMBER(10, 0),

    CONSTRAINT ITEM_PK PRIMARY KEY (ITEM_ID)
);


--------------------------------------------------------
--  TABLE CARD
--------------------------------------------------------

CREATE TABLE CARD (
    CARD_ID       NUMBER(10, 0),
    CARD_NAME     VARCHAR2(30) NOT NULL,
    DESCRIPTION   VARCHAR2(100) NOT NULL,
    EFFECT_TYPE   VARCHAR2(30) NOT NULL,
    EFFECT_VALUE  NUMBER(10, 0) DEFAULT 0 NOT NULL,

    CONSTRAINT CARD_PK PRIMARY KEY (CARD_ID)
);


--------------------------------------------------------
--  TABLE LAND
--------------------------------------------------------

CREATE TABLE LAND (
    LAND_ID       NUMBER(10, 0),
    LAND_TYPE     VARCHAR2(20),
    LAND_NAME     VARCHAR2(20) NOT NULL,
    BUY_PRICE     NUMBER(10, 0) DEFAULT 0 NOT NULL,
    TOLL_PRICE    NUMBER(10, 0) DEFAULT 0 NOT NULL,
    UPGRADE_COST  NUMBER(10, 0) DEFAULT 0 NOT NULL,
    UPGRADE_MAX   NUMBER(2, 0) DEFAULT 0 NOT NULL,

    CONSTRAINT LAND_PK PRIMARY KEY (LAND_ID)
);


--------------------------------------------------------
--  TABLE INVENTORY
--------------------------------------------------------

CREATE TABLE INVENTORY (
    USER_ID   VARCHAR2(20) NOT NULL,
    ITEM_ID   NUMBER(10, 0) NOT NULL,
    QUANTITY  NUMBER(10, 0) DEFAULT 0 NOT NULL,

    CONSTRAINT INVENTORY_PK PRIMARY KEY (USER_ID, ITEM_ID),
    CONSTRAINT INVENTORY_QTY_CK CHECK (QUANTITY >= 0),

    CONSTRAINT INVENTORY_USER_FK
        FOREIGN KEY (USER_ID)
        REFERENCES USERS (USER_ID),

    CONSTRAINT INVENTORY_ITEM_FK
        FOREIGN KEY (ITEM_ID)
        REFERENCES ITEM (ITEM_ID)
);


